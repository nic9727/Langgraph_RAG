def populate_mock_data() -> None:
    """向所有数据表中插入模拟数据（使用 INSERT OR IGNORE 避免重复）。"""
    conn = get_connection()

    conn.executemany(
        "INSERT OR IGNORE INTO students VALUES (?, ?, ?, ?, ?)",
        MOCK_STUDENTS,
    )
    conn.executemany(
        "INSERT OR IGNORE INTO courses VALUES (?, ?, ?, ?, ?, ?)",
        MOCK_COURSES,
    )
    conn.executemany(
        "INSERT OR IGNORE INTO enrollments (student_id, course_id, score, semester, status) VALUES (?, ?, ?, ?, ?)",
        MOCK_ENROLLMENTS,
    )
    conn.executemany(
        "INSERT OR IGNORE INTO grade_rules (rule_name, description, percentage, category) VALUES (?, ?, ?, ?)",
        MOCK_GRADE_RULES,
    )
    conn.executemany(
        "INSERT OR IGNORE INTO internship_companies (company_name, industry, positions, contact, cooperation_since, description) VALUES (?, ?, ?, ?, ?, ?)",
        MOCK_INTERNSHIP_COMPANIES,
    )

    conn.commit()
    conn.close()
    logger.info("模拟数据填充完成")


def init_database() -> None:
    """完整的数据库初始化流程：建表 + 填充模拟数据。"""
    init_tables()
